    public void login() throws ClassNotFoundException, SQLException{
        Class.forName("com.mysql.jdbc.Driver");
        java.sql.Connection con=DriverManager.getConnection("jdbc:mysql://localhost/loginswing","root", null);
        System.out.println("connection Successful");
        
        if (txtemail.getText()!= null && txtpass.getText()!= null) {
            String sql = "Select * from login where email='" + txtemail.getText() + "' and password='" + txtpass.getText() + "'";
            PreparedStatement stmt=con.prepareStatement(sql);    
            ResultSet rs = stmt.executeQuery(sql);
            if (rs.next()) {
                JOptionPane.showMessageDialog(null,"login Successful");
                
            } else {
            JOptionPane.showMessageDialog(null,"Incorrect password");
            }
        }
    
    }
